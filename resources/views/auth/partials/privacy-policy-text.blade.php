<p><strong>1. Introduction</strong><br>
Luntian HR (“the System”) is operated for authorized employees and administrators of Luntian. This Privacy Policy explains how we collect, use, store, and protect personal information when you use the System.</p>

<p><strong>2. Information we collect</strong><br>
We may process employment-related data including your name, employee ID, work email, department, position, contact details, government IDs (where applicable), compensation and bank information (restricted access), leave and attendance records, reimbursement submissions, incident reports, performance reviews, inventory allocations, and activity logs generated when you use the System.</p>

<p><strong>3. How we use your information</strong><br>
Your data is used to manage human resources operations, payroll-related processes, approvals, internal reporting, security monitoring, and compliance with company policies. Access is limited by role and department permissions.</p>

<p><strong>4. Storage and security</strong><br>
Data is stored on company-controlled servers. We apply authentication, session timeouts, access controls, and audit logging. Sensitive sections (such as compensation) may require additional confirmation before display.</p>

<p><strong>5. Sharing and disclosure</strong><br>
Personal information is not sold to third parties. It may be shared only with authorized personnel, service providers bound by confidentiality, or when required by law.</p>

<p><strong>6. Your responsibilities</strong><br>
You must keep your login credentials confidential, use only your assigned account, and report unauthorized access. You agree not to disclose confidential employee or company data obtained through the System.</p>

<p><strong>7. Data retention</strong><br>
Records are retained according to HR, legal, and operational requirements. Archived or inactive employee data may be kept for audit and compliance purposes.</p>

<p><strong>8. Your rights</strong><br>
You may request correction of inaccurate personal data through HR. Declining this Privacy Policy means you cannot use the System and your session will end.</p>

<p><strong>9. Updates</strong><br>
We may update this policy from time to time. Continued use after an update may require your acceptance again.</p>

<p><strong>10. Contact</strong><br>
For privacy-related concerns, contact your HR administrator or the Luntian HR team.</p>
